#include <stdio.h>

int main(void)
{
    int x = 5, y = 7, z = 9;
    int *a[3];    // 指標陣列
    int **p;      // 指向 int* 的指標（二重指標）

    a[0] = &x;
    a[1] = &y;
    a[2] = &z;

    printf("初始狀態：\n");
    printf("a[0]=%p → %d\n", (void*)a[0], *a[0]);
    printf("a[1]=%p → %d\n", (void*)a[1], *a[1]);
    printf("a[2]=%p → %d\n\n", (void*)a[2], *a[2]);

    // ---------------------------------------
    // a[0]++：改變 a[0] 內部的指標值
    // ---------------------------------------
    printf("執行 a[0]++:\n");
    a[0]++;  // 讓 a[0] 這格裡的指標往後移動一個 int 單位
    printf("a[0]=%p (現在指向未知區域)\n\n", (void*)a[0]);

    // ---------------------------------------
    // p = a；p++：改變陣列指標位置（相當於 a+1）
    // ---------------------------------------
    p = a;     // p 指向 a[0]
    printf("p = a;p 指向 a[0]\n");
    printf("*p = %p → %d\n", (void*)*p, **p); // *p = a[0]

    p++;       // p 現在指向 a[1]（等價於 a + 1）
    printf("\n執行 p++（等價於 a+1):\n");
    printf("*p = %p → %d\n", (void*)*p, **p); // *p = a[1]

    int** k = a+1;
    printf("k = %d\n", **k);
    return 0;
}
